        <p> Радио – самое оперативное медиа. Рекламные кампании на радио планируются и размещаются в
очень короткие сроки. Особенно это актуально для клиентов, у которых проводятся акции,
происходит частая смена продуктов и информационных сообщений, а также необходима
краткосрочная поддержка имиджа.</p>

